# Printing in python is pretty simple.
# To print something we need to use the print() function.
# Like this.

print("Hello, World!")
# This will print out Hello, World!
# To print variables you do this

name = "Jack"
print(name)
# Or you can add text and variable
print("Hi, my name is", name)
# This will print out Hi, my name is Jack

# You can also print numbers and equations.
print(1)
print(60 + 9)